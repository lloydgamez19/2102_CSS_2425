    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed
        // TODO add your handling code here:
        String enteredUsername = username.getText();
        String enteredPassword = new String(password.getPassword());
        
        System.out.println("Username: " + enteredUsername);
        System.out.println("Password: " + enteredPassword);
        
        if (authenticate(enteredUsername, enteredPassword)) {
            JOptionPane.showMessageDialog(this, "Login successful!");
            new AdminFrame().setVisible(true);
            dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Invalid username or password.", "Login Failed", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_loginButtonActionPerformed
    private boolean authenticate(String username, String password){
        System.out.println("Authenticating...");
        String dbUrl = "jdbc:mysql://localhost:3307/CATERING";
        String dbUsername = "root";
        String dbPassword = "Dikoalam.com3";
        String query = "SELECT * FROM users WHERE username = ? AND password = ?";
        
        try (Connection connection = DriverManager.getConnection(dbUrl, dbUsername, dbPassword);
                PreparedStatement statement = connection.prepareStatement(query)) {
            
            statement.setString(1, username); 
            statement.setString(2, password);
            
            try (ResultSet resultSet = statement.executeQuery()) {
                if (resultSet.next()){
                    return true;
                }
            }
        }   catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
         return false;
    }
