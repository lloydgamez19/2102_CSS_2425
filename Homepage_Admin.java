    private void btnRActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable3.getSelectedRow(); // Get the selected row index

    if (selectedRow == -1) {
        // No row is selected
        JOptionPane.showMessageDialog(this, "Please select a service to remove.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Show confirmation dialog
    int response = JOptionPane.showConfirmDialog(this, "Are you sure you want to remove this service?", "Confirm Removal", JOptionPane.YES_NO_OPTION);
    
    if (response == JOptionPane.YES_OPTION) {
        // Remove the selected row
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) jTable3.getModel();
        model.removeRow(selectedRow);
        JOptionPane.showMessageDialog(this, "Service removed successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
    }
    }//GEN-LAST:event_btnRActionPerformed

