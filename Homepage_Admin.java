    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        String itemName = txtIN.getText();
        String priceString = txtP.getText();
        String description = txtD.getText();

   
        try {
            double price = Double.parseDouble(priceString);
            javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) jTable3.getModel();
            model.addRow(new Object[]{itemName, description, price});
        
            JOptionPane.showMessageDialog(this, "Menu item added successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
        
        
            txtIN.setText("");
            txtP.setText("");
            txtD.setText("");
        
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Please enter a valid price.", "Error", JOptionPane.ERROR_MESSAGE);
    }

    }//GEN-LAST:event_btnCreateActionPerformed

